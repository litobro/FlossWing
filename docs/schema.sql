-- =============================================================================
-- Notes for implementers
-- =============================================================================
--
-- ON DELETE behavior summary:
--   - Deleting a run cascades to ~everything (findings, tasks, sessions, etc.)
--     This is intentional: a run is the unit of forensic interest, and
--     deleting it should leave no orphans.
--   - Deleting a finding cascades to its validations, traces, and the
--     dedupe_cluster where it's the primary. Findings linked as variants/
--     duplicates get their primary_finding_id set to NULL (SET NULL FK).
--   - agent_sessions referenced by validations/traces use ON DELETE RESTRICT
--     to preserve audit integrity. Run deletion cascades through the run_id
--     path before this triggers, so it's not normally blocking.
--
-- Performance assumptions:
--   - SQLite WAL mode is enabled at connection time (`PRAGMA journal_mode=WAL`)
--     for concurrent read access during scans.
--   - `PRAGMA synchronous=NORMAL` is acceptable; the state is reproducible from
--     the target repo if catastrophically lost mid-scan.
--   - Single-writer assumption holds: only the orchestrator writes; agents
--     write only via tool handlers which serialize through the orchestrator.
--
-- What's deliberately not here:
--   - No `users` table: single-user tool.
--   - No `projects` table: a run already knows its target_repo_path.
--   - No `prompts` table: prompts are version-controlled in flosswing/prompts/
--     and identified via system_prompt_hash on agent_sessions. If we want to
--     reproduce a scan, we check out the matching FlossWing version.
--   - No FTS tables: findings are reviewed in the report, not searched.
--     Could add FTS5 over findings.description in v2 if grep insufficient.
